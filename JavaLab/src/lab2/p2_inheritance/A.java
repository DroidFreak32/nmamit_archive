//Dynamic Method Dispatch & Method Overriding
package p2_inheritance;

public class A {
	public A (){
		System.out.println("In A default constructor");
	}
	
	public void callMe(){
		System.out.println("Inside A callMe");
	}
	public void callMeToo(){
		System.out.println("Inside A callMeToo");
	}
	
}


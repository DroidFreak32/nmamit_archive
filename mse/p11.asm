; Program to implement upcounter
CLRSCR MACRO 
MOV AH, 00H 
MOV AL, 02H
INT 10H 
ENDM 

CODE SEGMENT 
ASSUME CS:CODE 
START:
	CLRSCR 
	MOV AL,00H 
UP:
	CMP AL,99H
	JZ DISP99
	CALL CENTER
	MOV BL,AL
	CALL DISPHEXA
	CALL DELAY
	ADD AL, 01H
	DAA
	JMP UP
DISP99: 
	CALL CENTER 
	MOV BL, AL 
	CALL DISPHEXA
	CALL DELAY 
	MOV AH, 4CH 
	INT 21H

CENTER PROC NEAR
	PUSH AX 
	MOV DL, 39 
	MOV DH, 12 
	MOV BH,0 
	MOV AH,02H 
	INT 10H 
	POP AX 
	RET 
CENTER ENDP 

DISPHEXA PROC NEAR
	PUSH AX
	MOV DL, BL
	MOV CL,04
	SHR DL, CL
	CMP DL,09
	JBE DOWN1
	ADD DL,07
DOWN1:
	ADD DL,30H
	MOV AH,02H
	INT 21H
	MOV DL, BL
	AND DL,0FH
	CMP DL,09H
	JBE DOWN2
	ADD DL,07H
DOWN2:
	ADD DL,30H
	MOV AH,02H
	INT 21H
	POP AX
	RET
DISPHEXA ENDP

DELAY PROC
    PUSH BX
    MOV CX,0300H
L1:
	MOV BX,02FFH
L2:
	DEC BX
	JNZ L2
	LOOP L1
	POP BX
	RET
	DELAY ENDP
CODE ENDS
END START
